# Our very First Program
# Containers

# Single Value Container

# Create statement -> you are creating a svc in RAM

# user_name is Reference variable
user_name = "auribises"

# Read statement -> To read data inside the container
print(user_name,id(user_name),type(user_name))

# UPDATE STATEMENT

user_name = 1001

print(user_name,id(user_name),type(user_name))

# DELETE STATEMENT
del user_name   

print(user_name,id(user_name),type(user_name))
